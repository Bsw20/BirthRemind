import Foundation
import UIKit

extension UIColor {
    static func mainPurple() -> UIColor {
        return #colorLiteral(red: 0.7803921569, green: 0.6431372549, blue: 1, alpha: 1)
    }
    
    static func mainGray() -> UIColor {
        return #colorLiteral(red: 0.7411764706, green: 0.7411764706, blue: 0.7411764706, alpha: 1)
    }
    static func darkGray() -> UIColor {
        return #colorLiteral(red: 0.4274509804, green: 0.4274509804, blue: 0.4274509804, alpha: 1)
    }
}
